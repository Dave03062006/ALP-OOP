package model;
public abstract class User {
    protected String email;
    protected String password;
    protected String fullName;
    protected String phoneNumber;

    // Constructor
    public User(String email, String password, String fullName, String phoneNumber) {
        this.email = email;
        this.password = password;
        this.fullName = fullName;
        this.phoneNumber = phoneNumber;
    }

    // Abstract methods that must be implemented by all subclasses
    public abstract void displayInfo();
    public abstract boolean login(String email, String password);
    public abstract boolean register(String email, String password, String fullName, String phoneNumber);

    // Getters and Setters
    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}